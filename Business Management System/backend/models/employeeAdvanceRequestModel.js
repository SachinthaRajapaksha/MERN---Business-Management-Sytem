const mongoose = require('mongoose');

const EmployeeAdvanceRequestSchema = new mongoose.Schema({
  empId: {
    type: String,
    required: true
  },
  name: {
    type: String,
    required: true
  },
  amount: {
    type: Number,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  },
  status: {
    type: String,
    enum: ['pending', 'accepted', 'rejected'],
    default: 'pending'
  }
});

module.exports = mongoose.model('EmployeeAdvanceRequest', EmployeeAdvanceRequestSchema);

